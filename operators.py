# ------------------------------------
# -- Comparison Operators ---
# [ == ] Equal
# [ != ] Not Equal
# [ > ] Greater Than
# [ < ] Less Than
# [ >= ] Greater Than or Equal
# [ <= ] Less Than or Equal
#
# ----------------------------------

# Equal + Not Equal
print(100 == 100)
print(100 == 200)
print(100 == 100.00)

print("@" * 50)

print(100 != 100)
print(100 != 200)
print(100 != 100.00)
print("@" * 50)

# Greater Than + Less Than

print(100 > 100)
print(100 > 200)
print(100 > 100.00)
print(100 > 50)
print("@" * 50)
